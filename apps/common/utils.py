from string import Formatter


def minutes_to_seconds(minutes):
    return minutes * 60


class KWArgFormatter(Formatter):
    def get_value(self, key, args, kwds):
        if isinstance(key, str):
            try:
                return kwds[key]
            except KeyError:
                return key
        else:
            return Formatter.get_value(key, args, kwds)
